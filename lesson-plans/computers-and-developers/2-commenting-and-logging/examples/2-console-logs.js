'use strict';

/* Console Logs (dynamic information)

  `console.log` is an easy way to leave messages for developers in the console
  logging helps developers understand what's happening in a program while it runs

  Pro Tip!
    read the log's line number to find out where it came from in the code

*/

// log a string describing an important step in the program
console.log('-- the program is running --');

// log the different values stored in a variable
let name = 'Tara';
console.log(name); // 'Tara'

name = name + name;
// you can also log more than one thing using commas
console.log('double name:', name); // 'double name:', 'TaraTara'

console.log('-- the program is finished --');
